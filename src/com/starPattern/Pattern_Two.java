package com.starPattern;

public class Pattern_Two {
	
	public static void main (String args[])
	{
		
		for (int i=5 ;i>=0;i--)
		{
			System.out.print("*");
			
			for (int j=1;j<=5;j++)
			{
				System.out.print(" ");
			}
			for (int k=i;k<=i;k++)
			{
				System.out.println();
			}
		}
		
		
		
	}

}
